package com.fu.entity;

//资产实体类
public class Asset {

	private int zid;
	private String zname;
	private String zmoney;
	private String zcompony;
	private String zdate;
	public int getZid() {
		return zid;
	}
	public void setZid(int zid) {
		this.zid = zid;
	}
	public String getZname() {
		return zname;
	}
	public void setZname(String zname) {
		this.zname = zname;
	}
	public String getZmoney() {
		return zmoney;
	}
	public void setZmoney(String zmoney) {
		this.zmoney = zmoney;
	}
	public String getZcompony() {
		return zcompony;
	}
	public void setZcompony(String zcompony) {
		this.zcompony = zcompony;
	}
	public String getZdate() {
		return zdate;
	}
	public void setZdate(String zdate) {
		this.zdate = zdate;
	}
	
	
}
